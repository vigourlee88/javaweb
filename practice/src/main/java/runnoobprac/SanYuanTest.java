package runnoobprac;

public class SanYuanTest {
	public static  void main(String[] args) {
		int a , b;
		a = 10;
		//如果a等于1成立，则设置b为20，否则为30
		b = (a == 1) ? 20 : 30;
		System.out.println("Value of b is :" + b);
		
		//如果a等于10成立，则设置b为20，否则为30
		b = (a == 10) ? 20: 30;
		System.out.println("Value of b is :" + b);
		
		
	}
}