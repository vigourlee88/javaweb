public class BinaryTest {
    public static void main(String[] args) {

        int n1 = 0b1010;//二进制
        int n2 = 1010; //十进制
        int n3 = 01010;//八进制
        int n4 = 0x10101;//十六进
        System.out.println("n1="+n1);//10
        System.out.println("n2="+n2);//1010
        System.out.println("n3="+n3);//520
        System.out.println("n4="+n4);//65793
    }
}
